const mongoose = require('mongoose');

const commentScheme = new mongoose.Schema({
    content: {
        type: String,
        required: true
    },
    author: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    post: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Post'
    }
})
const Comment = mongoose.model('Comment', commentScheme)
module.exports = Comment